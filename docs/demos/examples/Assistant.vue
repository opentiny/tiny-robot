<template>
  <tr-container v-model:fullscreen="fullscreen" v-model:show="show" class="tiny-container">
    <template #operations>
      <button class="icon-btn" @click="handleNewSession">
        <icon-new-session />
      </button>
      <span style="display: inline-flex; line-height: 0; position: relative">
        <button class="icon-btn" @click="showHistory = true">
          <icon-history />
        </button>
        <tr-history
          v-show="showHistory"
          class="tr-history-demo"
          tab-title="历史对话"
          :selected="currentMessageId"
          :search-bar="true"
          :data="historyData"
          @close="showHistory = false"
          @item-click="handleHistorySelect"
        ></tr-history>
      </span>
    </template>
    <template v-if="messages.length === 0">
      <tr-welcome title="盘古助手" description="您好，我是盘古助手，您专属的华为云专家" :icon="welcomeIcon">
        <template #footer>
          <div class="welcome-footer">
            <span>根据相关法律法规要求，您需要先 <a>登录</a>，若没有帐号，您可前往 <a>注册</a></span>
          </div>
        </template>
      </tr-welcome>
      <tr-prompts
        :items="promptItems"
        :wrap="true"
        item-class="prompt-item"
        class="tiny-prompts"
        @item-click="handlePomptItemClick"
      ></tr-prompts>
    </template>
    <tr-bubble-list v-else :items="messages" :roles="roles"></tr-bubble-list>

    <template #footer>
      <div class="chat-input">
        <tr-suggestion
          :items="suggestItems"
          :categories="categories"
          @select="handleCommandSelect"
          :maxVisibleItems="5"
        >
          <template #trigger="{ onKeyDown, onInput }">
            <tr-sender
              mode="multiple"
              v-model="inputMessage"
              :placeholder="GeneratingStatus.includes(messageState.status) ? '正在思考中...' : '请输入您的问题'"
              :clearable="true"
              :loading="GeneratingStatus.includes(messageState.status)"
              :showWordLimit="true"
              :maxLength="1000"
              @submit="sendMessage"
              @cancel="abortRequest"
              @input="handleMessageInput($event, onInput)"
              @keydown="handleMessageKeydown($event, onKeyDown)"
            ></tr-sender>
          </template>
        </tr-suggestion>
      </div>
    </template>
  </tr-container>
  <div style="display: flex; flex-direction: column; gap: 8px">
    <div>
      <label>show：</label>
      <tiny-switch v-model="show"></tiny-switch>
    </div>
    <div>
      <label>fullscreen：</label>
      <tiny-switch v-model="fullscreen"></tiny-switch>
    </div>
  </div>
</template>

<script setup lang="ts">
// import { TrContainer, TrWelcome, TrPrompts, TrBubbleList, TrSender } from '@opentiny/tiny-robot'
import { type BubbleRoleConfig, type PromptProps, type SuggestionItem, type Category } from '@opentiny/tiny-robot'
import { AIClient, ChatMessage, GeneratingStatus, useMessage } from '@opentiny/tiny-robot-kit'
import { IconAi, IconHistory, IconNewSession, IconUser } from '@opentiny/tiny-robot-svgs'
import { h, nextTick, reactive, ref, toRaw, watch, type CSSProperties } from 'vue'

const client = new AIClient({
  provider: 'openai',
  // apiKey: 'your-api-key',
  defaultModel: 'gpt-3.5-turbo',
  apiUrl: location.origin + '/cdocs/tiny-robot/',
})

const fullscreen = ref(false)
const show = ref(true)

const aiAvatar = h(IconAi, { style: { fontSize: '32px' } })
const userAvatar = h(IconUser, { style: { fontSize: '32px' } })
const welcomeIcon = h(IconAi, { style: { fontSize: '48px' } })

const promptItems: PromptProps[] = [
  {
    label: '日常助理场景',
    description: '今天需要我帮你安排日程，规划旅行，还是起草一封邮件？',
    icon: h('span', { style: { fontSize: '18px' } as CSSProperties }, '🧠'),
    badge: 'NEW',
  },
  {
    label: '学习/知识型场景',
    description: '有什么想了解的吗？可以是“Vue3 和 React 的区别”！',
    icon: h('span', { style: { fontSize: '18px' } as CSSProperties }, '🤔'),
  },
  {
    label: '创意生成场景',
    description: '想写段文案、起个名字，还是来点灵感？',
    icon: h('span', { style: { fontSize: '18px' } as CSSProperties }, '✨'),
  },
]

const { messages, messageState, inputMessage, sendMessage, abortRequest } = useMessage({
  client,
  useStreamByDefault: true,
  initialMessages: [],
})

const randomId = () => Math.random().toString(36).substring(2, 15)

const currentMessageId = ref('')

const handlePomptItemClick = (ev: unknown, item: { description?: string }) => {
  sendMessage(item.description)
}

const roles: Record<string, BubbleRoleConfig> = {
  assistant: {
    placement: 'start',
    avatar: aiAvatar,
    maxWidth: '80%',
  },
  user: {
    placement: 'end',
    avatar: userAvatar,
    maxWidth: '80%',
  },
}

const handleNewSession = () => {
  messages.value = []
  currentMessageId.value = ''
}

watch(
  () => messages.value[messages.value.length - 1]?.content,
  () => {
    if (GeneratingStatus.includes(messageState.status)) {
      nextTick(() => {
        scrollToBottom()
      })
    }
  },
)

const showHistory = ref(false)

const historyData = reactive<
  {
    date: string
    items: {
      title: string
      id: string
      data: ChatMessage[]
    }[]
  }[]
>([])

watch(
  () => messages.value[messages.value.length - 1]?.content,
  () => {
    if (!messages.value.length) {
      return
    }

    if (messages.value.length === 1) {
      currentMessageId.value = randomId()
    }

    const allSessions = historyData.flatMap((item) => item.items)
    const currentSession = allSessions.find((item) => item.id === currentMessageId.value)

    const data = toRaw(messages.value)
    if (!currentSession) {
      const today = historyData.find((item) => item.date === '今天')
      if (today) {
        today.items.unshift({ title: messages.value[0].content, id: currentMessageId.value, data })
      } else {
        historyData.unshift({
          date: '今天',
          items: [{ title: messages.value[0].content, id: currentMessageId.value, data }],
        })
      }
    } else {
      currentSession.data = data
    }
  },
)

const handleHistorySelect = (item: { id: string; data: ChatMessage[] }) => {
  currentMessageId.value = item.id
  messages.value = item.data
  showHistory.value = false
}

const chatContainer = ref<HTMLElement | null>(null)

const scrollToBottom = () => {
  if (chatContainer.value) {
    chatContainer.value.scrollTop = chatContainer.value.scrollHeight
  }

  const containerBody = document.querySelector('div.tr-container__body')
  if (containerBody) {
    containerBody.scrollTo({
      top: containerBody.scrollHeight,
      behavior: 'smooth',
    })
  }
}

// 处理输入，检测命令触发
const handleMessageInput = (event, handleInput) => {
  const currentText = inputMessage.value

  handleInput(event, currentText)
}

const handleCommandSelect = (value) => {
  inputMessage.value = value
}

// 处理键盘事件
const handleMessageKeydown = (event, onKeyDown) => {
  onKeyDown(event)
}

// 常用指令
const commonCommands: SuggestionItem[] = [
  {
    id: 'image',
    text: '/image',
    value: '/image',
    description: '生成图像 - 根据文本描述创建图像',
  },
  {
    id: 'sql',
    text: '/sql',
    value: '/sql',
    description: 'SQL查询 - 编写SQL查询语句并获取解释',
  },
  {
    id: 'code',
    text: '/code',
    value: '/code',
    description: '生成代码 - 根据描述生成代码片段',
  },
  {
    id: 'translate',
    text: '/translate',
    value: '/translate',
    description: '翻译文本 - 将文本翻译成指定语言',
  },
  {
    id: 'explain',
    text: '/explain',
    value: '/explain',
    description: '解释代码 - 分析并解释代码片段',
  },
]

// 高级指令
const advancedCommands: SuggestionItem[] = [
  {
    id: 'debug',
    text: '/debug',
    value: '/debug',
    description: '调试代码 - 查找并修复代码中的问题',
  },
  {
    id: 'optimize',
    text: '/optimize',
    value: '/optimize',
    description: '优化代码 - 提升代码性能和质量',
  },
  {
    id: 'format',
    text: '/format',
    value: '/format',
    description: '格式化代码 - 按照规范格式化代码',
  },
]

// Suggestion 组件使用的建议项
const suggestCommands = [...commonCommands, ...advancedCommands]

// 初始化 suggestItems 为所有可用的命令项
const suggestItems = ref<SuggestionItem[]>(suggestCommands)

// 功能分类
const categories = ref<Category[]>([
  {
    id: 'common',
    label: '常用指令',
    items: commonCommands,
  },
  {
    id: 'advanced',
    label: '高级指令',
    items: advancedCommands,
  },
  {
    id: 'One',
    label: '高级指令1',
    items: advancedCommands,
  },
  {
    id: 'Two',
    label: '高级指令2',
    items: advancedCommands,
  },
])
</script>

